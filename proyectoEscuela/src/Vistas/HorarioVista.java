    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
        // TODO add your handling code here:

        if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el ID");
        }
        if (txtIngreso.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue la hora de ingreso");
        }
        if (txtSalida.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue la hora de salida");
        } else {

            Horario a = new Horario();

            a.setID_HORARIO(Integer.parseInt(txtID.getText()));
            a.setHORA_INGRESO(txtIngreso.getText());
            a.setHORA_SALIDA(txtSalida.getText());

            String mensaje = aBO.agregarHorario(a);
        }
    }//GEN-LAST:event_AgregarActionPerformed

