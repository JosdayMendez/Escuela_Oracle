    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
        // TODO add your handling code here:

        if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el ID");
        }
        if (txtIDCurso.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el ID del curso");
        }
        if (txtIDEstudiante.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue ID del estudiante");
        }
        if (txtNota.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue la Nota");
        }
        if (txtIDTrimestre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el Trimestre");
        } else {

            Nota a = new Nota();

            a.setID_NOTAL(Integer.parseInt(txtID.getText()));
            a.setID_CURSO(Integer.parseInt(txtIDCurso.getText()));
            a.setID_TRIMESTRE(Integer.parseInt(txtIDTrimestre.getText()));
            a.setID_ESTUDIANTE(Integer.parseInt(txtIDEstudiante.getText()));
            a.setNOTA(Integer.parseInt(txtNota.getText()));

            String mensaje = aBO.agregarNota(a);
        }
    }//GEN-LAST:event_AgregarActionPerformed

    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        // TODO add your handling code here:

        if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el ID");
        }
        if (txtIDCurso.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el ID del curso");
        }
        if (txtIDEstudiante.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue ID del estudiante");
        }
        if (txtNota.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el telefono");
        }
        if (txtIDTrimestre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el nombre del Estudiante");
        } else {

            Nota a = new Nota();

            a.setID_NOTAL(Integer.parseInt(txtID.getText()));
            a.setID_CURSO(Integer.parseInt(txtIDCurso.getText()));
            a.setID_TRIMESTRE(Integer.parseInt(txtIDTrimestre.getText()));
            a.setID_ESTUDIANTE(Integer.parseInt(txtIDEstudiante.getText()));
            a.setNOTA(Integer.parseInt(txtNota.getText()));

            String mensaje = aBO.ModificarNota(a);
        }
    }//GEN-LAST:event_ModificarActionPerformed

