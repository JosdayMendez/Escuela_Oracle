    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
        // TODO add your handling code here:

        if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el ID");
        }
        if (txtApellido1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue primer apellido");
        }
        if (txtApellido2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue elsegundo apellido");
        }
        if (txtIDMadre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el ID de la madre");
        }
        if (txtIDPadre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el ID del padre");
        }
        if (txtIDProvincia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el ID de la provincia");
        } 
         if (txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Agregue el nombre del estudiante");
        }else {

            Estudiante a = new Estudiante();

            a.setID_ESTUDIANTE(Integer.parseInt(txtID.getText()));
            a.setID_MADRE(Integer.parseInt(txtIDMadre.getText()));
            a.setID_PADRE(Integer.parseInt(txtIDPadre.getText()));
            a.setID_PROVINCIA(Integer.parseInt(txtIDProvincia.getText()));
            a.setNOMBRE(txtNombre.getText());
            a.setAPELLIDO1(txtApellido1.getText());
            a.setAPELLIDO2(txtApellido2.getText());
            
            

            String mensaje = aBO.agregarEstudiante(a);
        }
    }//GEN-LAST:event_AgregarActionPerformed

